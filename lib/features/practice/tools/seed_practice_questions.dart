/// Practice Questions Seed Script
/// ================================
/// Uploads 250 Islamic practice questions to Firestore collection
/// `questions_practice`.
///
/// HOW TO RUN:
///   Long-press the upload icon in the admin preview screen.
///   Use clearAndReseed() to wipe + re-upload if you improved data.
///
/// Structure per document:
///   { question, options:[4], correctIndex, category, difficulty }

import 'package:cloud_firestore/cloud_firestore.dart';

const _collection = 'questions_practice';

/// Upload all questions (non-destructive — adds new docs).
/// NOT idempotent — run once only, or use [clearAndReseed].
Future<void> seedPracticeQuestions() async {
  final firestore = FirebaseFirestore.instance;
  final questions = _allQuestions();
  print('[Seed] Uploading ${questions.length} questions to $_collection...');
  await _uploadBatches(firestore, questions);
  print('[Seed] Done.');
}

/// Clears all documents in [_collection] then re-seeds. Safe to run multiple times.
Future<void> clearAndReseedPracticeQuestions() async {
  final firestore = FirebaseFirestore.instance;
  print('[Seed] Clearing $_collection...');

  // Delete in batches of 500
  while (true) {
    final snap = await firestore.collection(_collection).limit(500).get();
    if (snap.docs.isEmpty) break;
    final batch = firestore.batch();
    for (final doc in snap.docs) {
      batch.delete(doc.reference);
    }
    await batch.commit();
    print('[Seed] Deleted ${snap.docs.length} docs...');
  }

  print('[Seed] Collection cleared. Re-seeding...');
  final questions = _allQuestions();
  await _uploadBatches(firestore, questions);
  print('[Seed] Done.');
}

Future<void> _uploadBatches(
    FirebaseFirestore firestore, List<Map<String, dynamic>> questions) async {
  const chunkSize = 500;
  for (var i = 0; i < questions.length; i += chunkSize) {
    final chunk = questions.sublist(
      i,
      (i + chunkSize).clamp(0, questions.length),
    );
    final batch = firestore.batch();
    for (final q in chunk) {
      final ref = firestore.collection(_collection).doc();
      batch.set(ref, q);
    }
    await batch.commit();
    print('[Seed] Committed ${i + chunk.length} / ${questions.length}');
  }
}

